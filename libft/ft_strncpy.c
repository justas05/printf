#include <stddef.h>

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char	*dest_copy;
	size_t	i;

	dest_copy = dest;
	i = 0;
	while (i++ < n && (*dest++ = *src++))
		;
	while (i++ < n)
		*dest++ = '\0';
	return (dest_copy);
}
